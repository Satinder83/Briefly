def summarize(headline, content):
    result = [None]
    error  = [None]

    def call():
        try:
            prompt = f"""Summarize the following news article in exactly 60-65 words.
Write in plain, clear North American journalism style.
Start directly with the news — no intro phrases like "This article..." or "The story...".

Headline: {headline}
Article: {content[:3000]}

60-65 word summary:"""
            response = model.generate_content(prompt)
            result[0] = response.text.strip()
        except Exception as e:
            error[0] = str(e)

    t = threading.Thread(target=call)
    t.start()
    t.join(timeout=120)  # 20 second hard limit

    if t.is_alive():
        return "[Summary timed out]"
    if error[0]:
        return f"[Summary error: {error[0][:100]}]"
    return result[0]

import requests
