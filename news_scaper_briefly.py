def safe_scrape(url, timeout_secs=6):
    try:
        headers = {"User-Agent": "Mozilla/5.0"}
        response = requests.get(url, timeout=timeout_secs, headers=headers)
        content = trafilatura.extract(response.text) or "[No content extracted]"
        return content[:5000]
    except requests.exceptions.Timeout:
        return "[Timed out]"
    except Exception as e:
        return f"[Scrape error: {e}]"

