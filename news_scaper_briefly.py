import requests
import feedparser
import trafilatura
import time
from datetime import datetime, timezone

total_added = 0

for source_name, feed_url in FEEDS:
    print(f"\n📡 [{source_name}] fetching feed...")
    try:
        try:
            feed_response = requests.get(feed_url, timeout=8, headers={"User-Agent": "Mozilla/5.0"})
            feed = feedparser.parse(feed_response.text)
        except requests.exceptions.Timeout:
            print(f"  ❌ Feed timed out, skipping")
            continue

        print(f"  Got {len(feed.entries)} entries")
        count = 0

        for entry in feed.entries:
            if count >= MAX_PER_FEED:
                break
            url   = entry.get("link", "").strip()
            title = entry.get("title", "").strip()
            if not url or not title:
                continue
            if url in existing_urls:
                print(f"  ⏭ SKIP: {title[:70]}")
                continue

            print(f"  Scraping: {url[:80]}")
            content = safe_scrape(url, timeout_secs=6)
            print(f"  Scraped: {len(content)} chars")

            print(f"  Summarizing...")
            time.sleep(5)                          # ← ADDED HERE
            summary = summarize(title, content)
            print(f"  Summary: {summary[:80]}")

            row = [
                datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC"),
                source_name, title, url, content[:5000],
                "PENDING",
                summary,
                "",
            ]
            print(f"  Writing to sheet...")
            sheet.append_row(row, value_input_option="RAW")
            existing_urls.add(url)
            count += 1
            total_added += 1
            print(f"  ✅ ADDED: {title[:70]}")
            time.sleep(0.5)

    except Exception as e:
        print(f"  ❌ FEED ERROR: {e}")

print(f"\n✅ Done. Total added: {total_added}")

